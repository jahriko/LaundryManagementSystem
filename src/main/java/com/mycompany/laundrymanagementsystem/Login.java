    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        var isUserExist = Database.context()
                .fetchExists(selectFrom(USER)
                        .where(USER.USERNAME.eq(usernameField.getText())));
        
        if (isUserExist) {
            var getUser = Database.context().select().from(USER).fetchSingle();
            if (getUser.get(USER.PASSWORD).equals(String.valueOf(pwField.getPassword()))) {
                Dashboard db = new Dashboard();
                db.setVisible(true);
                db.setLocationRelativeTo(null);
                setVisible(false);
                dispose();                
            } else {
                errorMsg.setText("Incorrect Password");
                usernameField.setText("");
                pwField.setText("");
            }

        } else {
            errorMsg.setText("User not found.");
            usernameField.setText("");
            pwField.setText("");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

